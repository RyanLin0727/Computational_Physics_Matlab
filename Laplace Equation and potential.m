V=zeros(100,100);
V(1,1)=1;
V(1,100)=0;
for k=1:1000000
    for i=2:99
        for j=2:99
            V(i,j)=(V(i,j+1)+V(i,j-1)+V(i+1,j)+V(i-1,j))/4;
        end
end
for i=2:99
    V(i,1)=(V(i,1)+V(i,2)+V(i-1,1)+V(i+1,1))/4;
    V(i,100)=(V(i,100)+V(i,99)+V(i-1,100)+V(i+1,100))/4;
    V(1,i)=(V(1,i)+V(1,i-1)+V(1,i+1)+V(2,i))/4;
    V(100,i)=(V(100,i)+V(100,i-1)+V(100,i+1)+V(99,i))/4;
end
V(100,1)=(V(100,1)*2+V(100,2)+V(99,1))/4;
V(100,100)=(V(100,100)*2+V(99,100)+V(100,99))/4;
end
contour(V,21);
